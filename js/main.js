/* =============================================
   Render functions
   ============================================= */
function crearTarjeta(ev) {
  const card = document.createElement("div");
  card.className = "evidencia-card";
  card.setAttribute("data-tipo", ev.tipo);

  const badge = document.createElement("div");
  badge.className = "card-badge";
  card.appendChild(badge);

  const body = document.createElement("div");
  body.className = "card-body";

  const titulo = document.createElement("h3");
  titulo.textContent = ev.competencia;
  body.appendChild(titulo);

  const desc = document.createElement("p");
  desc.className = "descripcion";
  desc.textContent = ev.descripcion;
  body.appendChild(desc);

  if (ev.imagen) {
    const imgWrap = document.createElement("div");
    imgWrap.className = "card-image-wrap";
    const img = document.createElement("img");
    img.src = ev.imagen;
    img.alt = ev.competencia;
    img.loading = "lazy";
    imgWrap.appendChild(img);
    body.appendChild(imgWrap);
  }

  if (ev.archivo) {
    const link = document.createElement("a");
    link.href = ev.archivo;
    link.target = "_blank";
    link.rel = "noopener noreferrer";
    link.textContent = "Ver evidencia";
    body.appendChild(link);
  }

  const reflexion = document.createElement("p");
  reflexion.className = "reflexion";
  reflexion.textContent = ev.reflexion;
  body.appendChild(reflexion);

  card.appendChild(body);
  return card;
}

function poblarGrillas() {
  const grids = {
    cosmovision:   document.getElementById("grid-cosmovision"),
    transferencia: document.getElementById("grid-transferencia"),
    integradora:   document.getElementById("grid-integradoras")
  };

  if (!grids.cosmovision) return;

  evidencias.forEach(ev => {
    const grid = grids[ev.tipo];
    if (grid) grid.appendChild(crearTarjeta(ev));
  });
}

/* =============================================
   Filter functionality
   ============================================= */
function inicializarFiltros() {
  const botones  = document.querySelectorAll(".filter-btn");
  const grupos   = document.querySelectorAll(".evidencias-group");
  const sinRes   = document.getElementById("no-results");

  if (!botones.length) return;

  botones.forEach(btn => {
    btn.addEventListener("click", () => {
      botones.forEach(b => b.classList.remove("active"));
      btn.classList.add("active");

      const filtro = btn.dataset.filter;

      grupos.forEach(grupo => {
        if (filtro === "all") {
          grupo.style.display = "";
          return;
        }
        const tipo = grupo.id.replace("cosmovision", "cosmovision")
                             .replace("transferencia", "transferencia")
                             .replace("integradoras", "integradora");
        const mapId = { cosmovision: "cosmovision", transferencia: "transferencia", integradoras: "integradora" };
        const groupTipo = mapId[grupo.id] || grupo.id;
        grupo.style.display = (groupTipo === filtro) ? "" : "none";
      });

      if (sinRes) {
        const alguno = [...grupos].some(g => g.style.display !== "none");
        sinRes.classList.toggle("hidden", alguno);
      }
    });
  });
}

/* =============================================
   Mobile navigation
   ============================================= */
function inicializarNavMovil() {
  const toggle = document.querySelector(".nav-toggle");
  const nav    = document.querySelector(".main-nav");

  if (!toggle || !nav) return;

  toggle.addEventListener("click", () => {
    const open = nav.classList.toggle("open");
    toggle.setAttribute("aria-expanded", String(open));
  });

  document.querySelectorAll(".nav-item.has-dropdown > .nav-link").forEach(link => {
    link.addEventListener("click", e => {
      if (window.innerWidth <= 720) {
        e.preventDefault();
        link.parentElement.classList.toggle("open");
      }
    });
  });
}

/* =============================================
   Init
   ============================================= */
document.addEventListener("DOMContentLoaded", () => {
  poblarGrillas();
  inicializarFiltros();
  inicializarNavMovil();
});
